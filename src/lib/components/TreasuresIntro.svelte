    work your way up the ranks until you hit 'S'. Getting to rank 'S' will take
    <strong>340 points</strong>.

    <br /><br />

    <span>
      To check your current rank, you will need to go either Daguerreo or the Treno Inn. In
      Daguerreo, you must go to the right-hand side of the area and go up a floor and talk to the
      "4-armed man". In Treno, you must go to the Treno Inn and talk to the woman who is downstairs.
    </span>

    <br /><br />

    <span>
      Glancing at this guide will probably scare you off due to the sheer amount of items and
      treasures, but not to worry because reaching Rank S
      <strong>doesn't require every single treasure and item to be found!</strong>
      Sure, you will need to find a decent chunk of them, but there are plenty to go around. The ones
      that you will definitely want to focus on are the Chocographs and areas that will be locked away
      through story progression. Those will be noted at the beginning of each section when appropriate.
    </span>

    <br /><br />

    <span>
      Locations will be noted by a Disc Number. On the original PS1 version, there were four discs.
      Below are reference points for each disc's start/ending areas. There will be spoiler-free tags
      put in place of locations if this is your first time playing the game:
    </span>

    <br /><br />

    <ul class="ml-8 list-disc">
      <li>Disc 1: Ends after the story events in Burmecia.</li>
      <li>Disc 2: Ends after the story events in your second visit of the Iifa Tree.</li>
      <li>Disc 3: Ends after the story events in Pandemonium.</li>
      <li>Disc 4: The endgame.</li>
    </ul>

    <br />

    Lastly, the flow of this guide will go through all the story areas first that you visit
    throughout the game in chornological order. Then, toward the bottom of the guide, you'll start
    to see the "post-game" areas or areas that have items/treasures that you can get at multiple
    different points in the game. Be very wary about the table headers as they depict the disc names
    in bold and red text. Places like Alexandria, Lindblum, and Treno will have different treasures
    depending on the current disc that you're on. Each table will depict what disc(s) certain
    treasures will be found in.

